// "B. 仕事" → "B"。先頭が "X. " 形式のときだけ注釈キーになる。
export function categoryLetter(category: string): string | null {
  const m = category.match(/^([A-Z])\.\s/);
  return m ? m[1] : null;
}

// カテゴリに対応する注釈HTML（無ければ undefined）。
// レター付き(B/C/D/F/G)は SECTION_NOTES、レター無しは CATEGORY_NOTES から解決。
export function noteForCategory(category: string): string | undefined {
  const letter = categoryLetter(category);
  if (letter && SECTION_NOTES[letter]) return SECTION_NOTES[letter];
  return CATEGORY_NOTES[category];
}
